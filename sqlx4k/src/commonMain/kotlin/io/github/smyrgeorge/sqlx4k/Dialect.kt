package io.github.smyrgeorge.sqlx4k

/**
 * Enum class representing the supported SQL dialects.
 *
 * This enumeration defines the types of SQL dialects that can be used
 * to interact with different relational database systems. Each dialect
 * corresponds to a specific database system.
 */
enum class Dialect {
    MySQL,
    PostgreSQL,
    SQLite,
    ;
}